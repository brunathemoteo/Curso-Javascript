function calcularOperacoes (num1, num2) {
    soma = num1 + num2
    subt = num1 - num2
    mult = num1 * num2
    div = num1/num2

    console.log(`Soma: ${soma}`)
    console.log(`Subtração: ${subt}`)
    console.log(`Multiplicação: ${mult}`)
    console.log(`Divisão: ${div}`)
}

calcularOperacoes(8,4)
calcularOperacoes(15,3)